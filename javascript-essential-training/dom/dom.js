// the browser is an object and the document it displays is an object too

// the browser has a long list of objects including the browser window, the document inside, navigation buttons, url, etc
// these objects are modeled in what is known as the Browser Object Model or BOM
// because these are objects, we can use JS to interact with them

// ex
// width of window
window.innerWidth;

// open new tab
window.open();

// get the document object
window.document;
// or just document when inside the window

// html tags within the documents are nodes
// the document has its own model the Document Object Model or DOM

// targeting items in the dom
// body element
document.body;

// document title
document.title;

// document url
document.url;
